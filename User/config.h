#ifndef __CONFIG_H__
#define __CONFIG_H__

#include "SYSTEM/sys.h"

// ============================================================
// 项目可调配置 — 集中管理所有开关和参数
//
// 修改这里不需要改代码，改完重新编译即可。
// 关闭不用的模块可以释放定时器资源和 Flash/RAM。
//
// 定时器分配（注意模块间冲突）：
//   Timer0 → 超声波脉宽测量 (hc_sr04.c)
//   Timer1 → UART 波特率 9600 (bt.c, 蓝牙)
//   Timer2 → 电机 PWM (motor.c)
//   LCD 和 SMG 共用 P0，不可同时开启
// ============================================================

// ---------- 按键消抖时间（毫秒）----------
// 机械按键抖动约 5-10ms，取 20ms 确保稳定
// 增大 → 更稳定但响应更慢；减小 → 更快但可能误触发
#define KEY_DEBOUNCE_MS   20

// ---------- 计数器上限 ----------
// 计数器显示范围 0~COUNT_MAX，红外数字键可递增
#define COUNT_MAX         99

// ---------- 红外遥控 (已由 AVD 避障替代) ----------
// 启用后占用: P3.2 (INT0), Timer0 模式2
// 当前版本红外功能已移除，AVD 模式使用超声波+电机自主避障
#define ENABLE_IR          0

// ---------- 超声波测距 ----------
// 启用后占用: P2.0 (ECHO), P2.1 (TRIG), Timer0 模式1
#define ENABLE_SR04        1

// ---------- 蓝牙遥控 ----------
// 启用后占用: P3.0(RXD), P3.1(TXD), Timer1 模式2 (UART波特率)
#define ENABLE_BT          1

// ---------- 显示设备（LCD 与 SMG 二选一）----------
// 两者都占用 P0 口 → 不可同时 =1
#define ENABLE_LCD         1   // LCD1602 (16×2 字符液晶)
#define ENABLE_SMG         0   // 数码管 (6位共阴, P0 段选)

// ---------- 辅助外设 ----------
#define ENABLE_LED         0   // 8 个 LED (P1 口)
                               // 注意: P1.2~P1.7 被电机占用，同时开启会冲突!
#define ENABLE_KEY         1   // 4 个独立按键 (P3.4~P3.7)
#define ENABLE_BEEP        1   // 蜂鸣器 (P2.3)

// ---------- 小车电机 ----------
// 启用后占用: P1.2~P1.7, Timer2
#define ENABLE_MOTOR       1

#endif
