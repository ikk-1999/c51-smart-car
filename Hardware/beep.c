#include "Hardware/beep.h"

// ============================================================
// 蜂鸣器驱动实现 (P2.3, 有源蜂鸣器, 低电平响)
//
// 有源蜂鸣器：通电即响（自带振荡电路），不需要 PWM。
// 控制逻辑：
//   BEEP_PIN = 0 → NPN 三极管导通 → 蜂鸣器响
//   BEEP_PIN = 1 → 三极管截止 → 蜂鸣器停
//
// ⚠️ 注意事项：
//   1. 按键反馈蜂鸣: Beep_On 后 ~10ms 即 Beep_Off（主循环
//      首行），可闻但极短。如需更长按键反馈，可在 main.c 中
//      增加延时。
//   2. AVD 避障告警: 在 avd_task 的 BACK 状态期间每轮循环
//      持续调用 Beep_On()，用户可清晰听到。
//   3. 连续长时间响 (如 >10s) 可能损坏有源蜂鸣器。
// ============================================================

void Beep_Init(void)
{
    BEEP_PIN = 1;    // 初始关断（准双向口写1 → PNP/PNP 三极管截止）
}

void Beep_On(void)
{
    BEEP_PIN = 0;    // 拉低 → 三极管导通 → 蜂鸣器响
}

void Beep_Off(void)
{
    BEEP_PIN = 1;    // 拉高 → 三极管截止 → 蜂鸣器停
}
