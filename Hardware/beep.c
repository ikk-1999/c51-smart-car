#include "Hardware/beep.h"

// ============================================================
// 蜂鸣器驱动实现 (P2.3, 有源蜂鸣器, 低电平响)
//
// 有源蜂鸣器：通电即响（自带振荡电路），不需要 PWM。
// 控制逻辑：
//   BEEP_PIN = 0 → NPN 三极管导通 → 蜂鸣器响
//   BEEP_PIN = 1 → 三极管截止 → 蜂鸣器停
//
// ⚠️ 注意事项：
//   1. main.c 中 Beep_On 后只持续 ~10ms（下一轮主循环开头
//      就 Beep_Off），人耳几乎听不到。如果需要用户能听到，
//      应该改为"开 → 延时 → 关"或用一个计时变量控制时长。
//   2. 连续长时间响 (如 >10s) 可能损坏有源蜂鸣器。
// ============================================================

void Beep_Init(void)
{
    BEEP_PIN = 1;    // 初始关断（准双向口写1 → PNP/PNP 三极管截止）
}

void Beep_On(void)
{
    BEEP_PIN = 0;    // 拉低 → 三极管导通 → 蜂鸣器响
}

void Beep_Off(void)
{
    BEEP_PIN = 1;    // 拉高 → 三极管截止 → 蜂鸣器停
}
