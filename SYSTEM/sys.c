#include "SYSTEM/sys.h"

// ============================================================
// 系统初始化 — 上电后最先调用
//
// 做了什么：
//   1. EA=0 — 关总中断，防止初始化过程中被意外中断
//   2. TCON=0x00 — 清所有定时器/外部中断标志
//      (TF1, TR1, TF0, TR0, IE1, IT1, IE0, IT0)
//   3. SCON=0x00 — 清串口控制寄存器（波特率、中断等）
//
// 没做什么（故意留给各模块自己做）：
//   - 不配 TMOD → ir.c 初始化时设 Timer0 模式2, hc_sr04.c 初始化时设 Timer1 模式1
//   - 不配定时器重装值 → 各驱动自行设定
//   - 不开任何中断 → 所有硬件模块初始化完后 main() 才调 INT_ENABLE_GLOBAL()
//
// STC89C52 特有：
//   如需关闭看门狗（默认上电关），可取消注释 WDT_CONTR = 0x00
// ============================================================
void Sys_Init(void)
{
    EA = 0;

    TCON = 0x00;
    SCON = 0x00;

    // 如需关闭 STC 看门狗：
    // WDT_CONTR = 0x00;
}
