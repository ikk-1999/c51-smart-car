#include "SYSTEM/delay.h"
#include "SYSTEM/sys.h"

// ============================================================
// 纯软件延时（不占用任何硬件定时器）
//
// 11.0592MHz: 机器周期 ≈ 1.085μs
// delay_ms: 外层 ms 次，内层 114 次 ≈ 1ms @ 11.0592MHz
// delay_us: 空循环，适合极短延时
//
// 注意：软件延时精度受中断影响，不适合需要精确定时的场合
// ============================================================

void delay_ms(unsigned int ms)
{
    unsigned int i;
    do {
        i = 114;
        while (--i);
    } while (--ms);
}

// ============================================================
// 微秒软件延时
// us=1 → 约 1μs @ 11.0592MHz (最小约 2μs 开销)
// ============================================================
void delay_us(unsigned char us)
{
    while (--us);
}
